    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        //accede a la tabla y ponerle los valores
        //jTable es la tabla creada en la UI de ManageUser
        DefaultTableModel model = (DefaultTableModel) tableUser.getModel();
        //hay que poner try catch porque se va a conectar a la base de datos 
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from appuser where userRole='Admin'");
            //next recorre todos los valores
            //con getString se accede al valor de la columna
            //estos son os campos que se van a mostrar en la tabla
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("appuser_pk"), rs.getString("name"), rs.getString("mobileNumber"), rs.getString("email"), rs.getString("address"), rs.getString("status")});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        //deshabilitar el botón Update para que sólo pueda guardarse la nueva información agregada en el formulario
        btnUpdate.setEnabled(false);
    }//GEN-LAST:event_formComponentShown

