    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String name = txtName.getText();
        String mobileNumber = txtMobileNumber.getText();
        String email = txtEmail.getText();
        String address = txtAddress.getText();
        String status = (String) comboBoxStatus.getSelectedItem();

        if (validateFields("edit")) {
            JOptionPane.showMessageDialog(null, "All fields are required");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                //PreparedStatement actualiza los datos dinámicamente
                PreparedStatement ps = con.prepareStatement("update appuser set name=?, mobileNumber=?, email=?, address=?, status=? where appuser_pk=?");
                //establecer los valores
                ps.setString(1, name);
                ps.setString(2, mobileNumber);
                ps.setString(3, email);
                ps.setString(4, address);
                ps.setString(5, status);
                ps.setInt(6, appuserPk);

                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "User updated successfully");
                setVisible(false);
                new ManageUser().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    //cuando se haga click en una fila se llenen los campos del formulario con los datos de dicha fila
    private void tableUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableUserMouseClicked
        int index = tableUser.getSelectedRow();
        TableModel model = tableUser.getModel();

        //el segundo parámetro indica la columna (en este caso es la columna 0)
        //toString convierte a string
        String id = model.getValueAt(index, 0).toString();

        //parseInt convierte valores string a int y después la guarda en appuser
        appuserPk = Integer.parseInt(id);

        String name = model.getValueAt(index, 1).toString();
        //setText pone en el campo nombre (del formulario) el valor nombre de la fila en la cual se hizo click
        txtName.setText(name);

        String mobileNumber = model.getValueAt(index, 2).toString();
        txtMobileNumber.setText(mobileNumber);

        String email = model.getValueAt(index, 3).toString();
        txtEmail.setText(email);

        String address = model.getValueAt(index, 4).toString();
        txtAddress.setText(address);

        String status = model.getValueAt(index, 5).toString();
        comboBoxStatus.removeAllItems();

        if (status.equals("Active")) {
            comboBoxStatus.addItem("Active");
            comboBoxStatus.addItem("Inactive");
        } else {
            comboBoxStatus.addItem("Inctive");
            comboBoxStatus.addItem("Active");
        }

        //setEditable(false) indica que no se puede escribir nada en el campo password 
        txtPassword.setEditable(false);
        //se cambia el color del campo a gris
        txtPassword.setBackground(Color.DARK_GRAY);

        //ihabilita el botón save
        btnSave.setEnabled(false);
        //habilita el botón update
        btnUpdate.setEnabled(true);


    }//GEN-LAST:event_tableUserMouseClicked

